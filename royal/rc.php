<h5 class="text-center">¿Qué es el RC LOYALTY PROGRAM?</h5>
<p>
    RC Loyalty Program es el programa de fidelización y recompensa de Royal Concierge que permite a todos sus socios y afiliados generar y ganar puntos realizando sus reservas y compras a través de las “Empresas Aliadas, Marcas y Canales de Venta” disponibles en RC CLUB. Los socios afiliados a RC LOYALTY PROGRAM podrán consultar su estado de puntos, comprar y canjearlos en la plataforma de afiliados. Estos puntos podrán ser canjeados por viajes, regalos, descuentos, dinero y muchas otras recompensas. 
</p>

      <h1 class="text-center">Emblema Hotels & Travel Group</h1>
      <strong>EMBLEMA H&T Group </strong> inicia su andadura en el año 2012 poniendo en marcha la marca <strong>Emblema Hoteles </strong>
      con la finalidad de prestar servicios a Hoteles independientes para que pudieran competir en igualdad de condiciones con las principales cadenas hoteleras a escala internacional. En 2017 creamos la marca
      <strong>Emblema Viajes</strong> división de viajes dedicada, inicialmente a la distribución y comercialización de los Hoteles y Establecimientos turísticos a través de nuestra propia Central de Reservas. Posteriormente creando sinergias y acuerdos con mayoristas, bedbanks y B2B’s aumentamos nuestra oferta de servicios y productos turísticos ofreciendo variedad y cantidad sin sacrificar por ello la calidad y garantía de satisfacción a nuestros clientes y proveedores afiliados.
      En la actualidad seguimos siendo una organización dinámica y joven en constante crecimiento y sólida que ha sabido crecer adaptándose a los tiempos y que demuestra una tradición y una constancia de esfuerzo, servicio, espíritu creativo e innovación a lo largo de los años. 
      <br><br>
      <strong>EMBLEMA H&T Group </strong>se segmenta en dos grandes áreas que siguen caminos paralelos, si bien aprovechan mutuamente las sinergias que emanan ambas actividades:
      <br>
      <ul>
        <li><strong>Área Viajes: Emblema Viajes</strong>, es nuestra marca dedicada a la distribución y venta de servicios turísticos a través de más de 20 agencias online propias especializadas en viajes y reservas hoteleras además de otras 150 agencias de viajes online y más de 600.000 agencias de viajes en todo el mundo. Actualmente abarcamos todos los mercados y continentes existentes a través de nuestras redes, contratos y acuerdos con los principales canales y protagonistas en el sector. </li>
        <li> <strong> Área Hoteles: Emblema Hoteles</strong>, nuestra marca especializada en la comercialización, distribución y administración de establecimientos turísticos y vacacionales. Actualmente ofrecemos diferentes modelos de negocio a hoteles independientes a través de acuerdos de Franquicia, Management, Alquiler, Operación, Distribución y Revenue Management. </li>
      </ul>
      Estas son otras marcas que forman parte de nuestra organización y que complementan otras necesidades del sector, de nuestros partners y afiliados:
      <br><br>
      <ul>
        <li><strong>Royal Concierge: Fidelización y Servicio personalizado</strong>. El Concierge virtual es nuestra solución a la ausencia o falta de presencia de esta emblemática figura en hoteles de categoría media o baja o sin la posibilidad de disponer de un concierge entre su staff por falta de presupuesto. Royal Concierge asiste virtualmente a cada huésped antes, durante y post estadía en el hotel o establecimiento. Realiza una labor completa de fidelización y ventas cruzadas (cross/upselling) de servicios alternativos que ofrece cada hotel o destino, además de hacerle participar en el programa de puntos ROYAL´s. Utiliza todos los medios de comunicación disponibles, chat personalizado, whatsapp, Skype, Messenger y llamadas telefónicas.  </li>
        <li><strong>Dotahoteles: Central de Compras</strong>. Nuestra marca dedicada a conseguir los mejores proveedores y los mejores precios para abastecer los servicios de los hoteles en las mejores condiciones de mercado.</li>
        <li><strong>HotelComunity: Marketing y Comunicación</strong>. Nuestra Red Social creada por y para todos los hoteleros y profesionales del sector que quieran compartir, debatir y promover ideas, opiniones y posturas. Un canal que proyectará la voz y palabras de miles de profesionales de nuestro sector con el objetivo que la unión hace la fuerza.</li>
        <li><strong>Emblema Learning Center: E-learning y cursos online</strong>. Nuestro centro de capacitación y formación online en materia de turismo dirigido a personal de base, mandos intermedios y directivos.
          <ul>
            <li>Si tu objetivo es <strong> trabajar en Hostelería y/o Turismo </strong> y no tiene formación previa. </li>
            <li>Si deseas <strong>actualizar</strong> tu formación en algún área en concreto.</li>
            <li>Si eres estudiante de Hostelería y/o Turismo y deseas <strong> reforzar </strong> tu formación aplicada en temas específicos.</li>
            <li>Si eres profesional en activo y tienes interés en <strong> complementar</strong> tu experiencia.</li>
            <li>Si deseas <strong> incorporarte a este campo </strong> desde otros sectores profesionales.</li>
            <li>Si deseas <strong> emprender </strong> un negocio relacionado con Hostelería y/o Turismo.</li>
          </ul>
        </li>
        <li><strong>Hotelmanager: Innovación y tecnología</strong>. Nuestra marca dedicada a ofrecer soluciones tecnológicas a hoteles y otros establecimientos turísticos. Nuestro objetivo, simplificar la gestión hotelera a través de las ultimas novedades en soportes tecnológicos especializados en nuestro sector a hoteles de cualquier categoría, tamaño o perfil. Herramientas como PMS, Channel Manager, CRS, Motor de reservas, Apps, Programas de fidelización y muchos soportes mas al servicio utilizados como medio, no como fin del hotelero. </li>
      </ul>
